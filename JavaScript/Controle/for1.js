/*
 *Aula sobre estrutura de repetição for
 */
let contador = 1
while(contador <= 10){
    console.log(`Contador = ${contador}.`)
    contador++
}

for(let i = 1; i <= 10 ;i++){
    console.log(`I = ${i}.`)
}

const notas = [6.7,7.4,9.8,8.1,7.7]
for(let a = 0; a < notas.length; a++){
    console.log(`Notas = ${notas[a]}`)
}