/*
Crie uma função que irá receber dois valores, o
dividendo e o divisor. A função deverá imprimir
o resultado e o resto da divisão destes dois
valores.
*/

resultadoFinal = (dividendo, divisor) => console.log(`O resultado é: ${dividendo/divisor} e o resto da conta é: ${dividendo%divisor}`)

